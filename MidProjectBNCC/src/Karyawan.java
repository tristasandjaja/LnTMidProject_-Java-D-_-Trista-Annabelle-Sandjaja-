
public class Karyawan {
String kode;
String nama;
String kelamin;
String jabatan;
int gaji;
public Karyawan(String kode, String nama, String kelamin, String jabatan, int gaji) {
	super();
	this.kode = kode;
	this.nama = nama;
	this.kelamin = kelamin;
	this.jabatan = jabatan;
	this.gaji = gaji;
}

}
